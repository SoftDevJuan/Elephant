    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Funciones consultar=new Funciones();
        if(!Jcb_Cat.getSelectedItem().toString().equals("")){
            consultar.setCategoria(Jcb_Cat.getSelectedItem().toString());
            Jt_Producto.setModel(consultar.ConsultarCat());
        }else if(Jcb_Cat.getSelectedItem().toString().equals("")&&Jcb_Tienda.getSelectedItem().toString().equals("")){
            Jt_Producto.setModel(consultar.Consultar());
        }else if(!Jcb_Tienda.getSelectedItem().toString().equals("")&&Jcb_Cat.getSelectedItem().toString().equals("")){
            consultar.setNmtienda(Jcb_Tienda.getSelectedItem().toString());
            Jt_Producto.setModel(consultar.ConsultarTienda());
        }else if(!Jcb_Tienda.getSelectedItem().toString().equals("")&&!Jcb_Cat.getSelectedItem().toString().equals("")){
            consultar.setCategoria(Jcb_Cat.getSelectedItem().toString());
            consultar.setNmtienda(Jcb_Tienda.getSelectedItem().toString());
            Jt_Producto.setModel(consultar.ConsultarTienda_Cat());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

